    private void tb_saidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_saidaMouseClicked

        if (evt.getClickCount() != 2) {
            limparCampos();
            btn_editar.setEnabled(false);
            btn_excluir.setEnabled(false);
            js_nTabela.setValue(0);
        }

        if (evt.getClickCount() == 2) {
            int linha = tb_saida.getSelectedRow();
            preencherCampos(linha);
            TbGTASaida.setNumRows(0);
            SaidaD.buscarRegistroGTA(TbGTASaida, Integer.parseInt(TbSaida.getValueAt(linha, 0).toString()));
            js_nTabela.setValue(TbGTASaida.getRowCount());
            habilitarCampos();
            txt_idSaida.setEnabled(false);
            btn_excluir.setEnabled(true);
            btn_editar.setEnabled(true);
            btn_Salvar.setEnabled(false);
        }


    }//GEN-LAST:event_tb_saidaMouseClicked

