    final void somarGTAm() {
        int linha = tb_gta.getSelectedRow();
        int quant = 0;
        try {
            for (int i = 4; i <= 7; i++) {
                if (tb_gta.getValueAt(linha, i).equals(0)) {
                    tb_gta.setValueAt(0, linha, i);
                }
                quant += Integer.parseInt(tb_gta.getValueAt(linha, i).toString());
            }
            tb_gta.setValueAt(quant, linha, 8);
        } catch (Exception e) {
        }

    }

