    private boolean verificarCampos() {
        for (int i = 0; i < tb_gta.getRowCount(); i++) {
            int nDigitosGTA = TbGTASaida.getValueAt(i, 0).toString().length();
            if (nDigitosGTA != 6) {
                JOptionPane.showMessageDialog(null, "O campo Nº GTA deve conter 6 digitos", "Erro", 0, FormatarICO.ICObtnSair());
                return false;
            }
            int QmTotal = Integer.parseInt(TbGTASaida.getValueAt(i, 8).toString());
            int QfTotal = Integer.parseInt(TbGTASaida.getValueAt(i, 13).toString());
            if ((QmTotal + QfTotal) != Integer.parseInt(TbGTASaida.getValueAt(i, 3).toString()) || TbGTASaida.getValueAt(i, 3).toString().equals("0")) {
                JOptionPane.showMessageDialog(null, "Verifique a distribuíção das eras no GTA nº " + TbGTASaida.getValueAt(i, 0), "Erro", 0, FormatarICO.ICObtnSair());
                return false;
            }
            if (tb_gta.getValueAt(i, 1).toString().equals("-") || tb_gta.getValueAt(i, 2).toString().equals("-")) {
                JOptionPane.showMessageDialog(null, "Verifique o campo Origem e Destino no GTA nº " + TbGTASaida.getValueAt(i, 0), "Erro", 0, FormatarICO.ICObtnSair());
                return false;
            }
        }

        if (txt_dtSaida.getDate() == null) {
            JOptionPane.showMessageDialog(null, "O campo data deve conter uma data válida!", "Erro", 0, FormatarICO.ICObtnSair());
            return false;
        }
        if (Corretores.verificaDataMensal(txt_dtSaida.getDate())) {
        } else {
            return false;
        }

        if (cb_motivo.getSelectedItem().equals("-")) {
            JOptionPane.showMessageDialog(null, "O campo motivo deve ser preenchido!", "Erro", 0, FormatarICO.ICObtnSair());
            return false;
        } else if (cb_motivo.getSelectedItem().equals("Venda") & txt_nEscala.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Caso a saída seja uma venda, o campo Nº Escala deve estar preenchido!", "Erro", 0, FormatarICO.ICObtnSair());
            return false;
        }

        if (txt_qNF.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo Quantidade na Nota Fiscal deve ser diferente de nulo!", "Erro", 0, FormatarICO.ICObtnSair());
            return false;
        }
        if (txt_qMin.getText().equals("") || txt_qMin.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "O campo Quantidade na Minuta deve ser diferente de nulo e zero!", "Erro", 0, FormatarICO.ICObtnSair());
            return false;
        }

        if (cb_motivo.getSelectedItem().equals("Morte") || cb_motivo.getSelectedItem().equals("Abate Interno")) {
            if (cb_origGado.getSelectedItem().equals("-") || cb_categoria.getSelectedItem().equals("-")) {
                JOptionPane.showMessageDialog(null, "Os campos Origem e Categoria são obrigatórios !", "Erro", 0, FormatarICO.ICObtnSair());
                return false;
            }
        } else {
            if (cb_origGado.getSelectedItem().equals("-") || cb_destino.getSelectedItem().equals("-") || cb_categoria.getSelectedItem().equals("-")) {
                JOptionPane.showMessageDialog(null, "Os campos Origem, Destino e categoria são obrigatórios !", "Erro", 0, FormatarICO.ICObtnSair());
                return false;
            }
        }
        if (!cb_transp.getSelectedItem().equals("-") & txt_nMin.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo Nº Minuta é obrigatório para caso de transporte", "Erro", 0, FormatarICO.ICObtnSair());
            return false;
        }
        return true;
    }

