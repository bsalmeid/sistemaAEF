    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
        int cadastrar = JOptionPane.showConfirmDialog(null, "Deseja Salvar esta Saída de Gado?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (cadastrar == JOptionPane.YES_OPTION) {
            if (verificarCampos() == true) {
                popularBeans();
                if (SaidaD.inserirGta(TbGTASaida, SaidaB)) {
                    SaidaD.preencherTabela(SaidaB, TbSaida);
                    limparCampos();
                    desabilitarCampos();
                }
            }
        }
    }//GEN-LAST:event_btn_SalvarActionPerformed

    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        int editar = JOptionPane.showConfirmDialog(null, "Deseja editar esta Saída de Gado?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (editar == JOptionPane.YES_OPTION) {
            if (!txt_idSaida.getText().equals("")) {
                popularBeans();
                if (verificarCampos() == true) {
                    SaidaD.editarRegistro(TbGTASaida, Integer.parseInt(txt_idSaida.getText()), SaidaB);
                    SaidaD.preencherTabela(SaidaB, TbSaida);
                    limparCampos();
                    desabilitarCampos();
                    btn_editar.setEnabled(false);
                    btn_excluir.setEnabled(false);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Selecione a Saída que deseja editar!", "Erro", 0, FormatarICO.ICObtnSair());
            }
        }
    }//GEN-LAST:event_btn_editarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        int excluir = JOptionPane.showConfirmDialog(null, "Deseja excluir esta Saída de Gado?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (excluir == JOptionPane.YES_OPTION) {
            SaidaC.excluirRegistroController(Integer.parseInt(txt_idSaida.getText()));
            limparCampos();
            desabilitarCampos();
            btn_editar.setEnabled(false);
            btn_excluir.setEnabled(false);
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

    private void cb_motivoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cb_motivoItemStateChanged
        cb_destino.removeAllItems();
        BoxDestino.removeAllItems();
        if (cb_motivo.getSelectedItem().toString().equals("Transf (-)")) {
            ArrayList FazendaDestino = SaidaD.preencherFazendaDestino();
            cb_destino.setModel(new DefaultComboBoxModel(new Vector(FazendaDestino)));
            BoxDestino.setModel(new DefaultComboBoxModel(new Vector(FazendaDestino)));
            ColumnDestino.setCellEditor(new DefaultCellEditor(BoxDestino));
            txt_nEscala.setText("0");
            txt_nEscala.setEnabled(false);
        } else if (cb_motivo.getSelectedItem().toString().equals("Venda")) {
            ArrayList clientes = SaidaD.buscarClientes();
            cb_destino.setModel(new DefaultComboBoxModel(new Vector(clientes)));
            BoxDestino.setModel(new DefaultComboBoxModel(new Vector(clientes)));
            ColumnDestino.setCellEditor(new DefaultCellEditor(BoxDestino));
            txt_nEscala.setText("0");
            txt_nEscala.setEnabled(true);
        } else {
            cb_destino.addItem("-");
        }
    }//GEN-LAST:event_cb_motivoItemStateChanged

