    final void calcularPesoM() {
        double PesoL = 0;
        double PesoM = 0;
        int nCab = 0;
        try {
            PesoL = Corretores.ConverQuilosDouble(txt_pesoB.getText()) - Corretores.ConverQuilosDouble(txt_tara.getText());
            String Liquido = new DecimalFormat("#,##0.00").format(PesoL);
            txt_pesoL.setText(String.valueOf(Liquido));
        } catch (Exception e) {
            //  JOptionPane.showMessageDialog(null, "O campo peso bruto e tarda devem conter um númerno no formato 0.00", "Erro", 0, new ImageIcon("imagens/btn_sair.png"));
        }
        try {
            nCab = Integer.parseInt(txt_qMin.getText());
            if (nCab > 0) {
                PesoM = (PesoL / nCab);
                String media = new DecimalFormat("#,##0.00 kg").format(PesoM);
                txt_pesoM.setText(String.valueOf(media));
            } else {
                String media = new DecimalFormat("#,##0.00 kg").format(PesoM);
                txt_pesoM.setText(String.valueOf(media));
            }
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, "O campo número de cabeças deve conter um número inteiro", "Erro", 0, new ImageIcon("imagens/btn_sair.png"));
        }
    }

    private void totalizarResumo() {
        int totalGTA = 0;
        int totalMin = 0;
        double pesoTotal = 0.0;
        for (int i = 0; i < TbSaidaResumo.getRowCount(); i++) {
            totalGTA += Integer.parseInt(TbSaidaResumo.getValueAt(i, 17).toString());
            totalMin += Integer.parseInt(TbSaidaResumo.getValueAt(i, 18).toString());
            pesoTotal += (Corretores.ConverQuilosDouble(TbSaidaResumo.getValueAt(i, 13).toString()));
        }

        txt_totalGTA.setText(String.valueOf(totalGTA));
        txt_totalMin.setText(String.valueOf(totalMin));
        txt_pesoMTotal.setText(new DecimalFormat("0.00 kg").format(pesoTotal / totalMin));

    }

