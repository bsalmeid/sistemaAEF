    private Integer getIDContaOrigem(JComboBox comboBox) {
        if (comboBox.getSelectedIndex() != 0) {
            return listContaOrigem.get(comboBox.getSelectedIndex() - 1).getIdConta();
        }
        return 0;
    }

    private Date getDataAtual() {
        Date d = new Date(System.currentTimeMillis());
        Calendar c = Calendar.getInstance();
        //c.add(Calendar.DAY_OF_WEEK, 0);
        d = c.getTime();
        return d;
    }

    private String getOrderBy(){
        String s = cb_ordem.getSelectedItem().toString();
        if (s.equals("Data")){
            return "dataMov";
        } else if (s.equals("Favorecido")){
            return "Nome";
        } else if (s.equals("Nº Documento")){
            return "nDocPagto";
        } else if (s.equals("")){
            return "";
        }
        return "dataMov";
    }
    
