    private void btn_gerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_gerarActionPerformed
        int confirma = JOptionPane.showConfirmDialog(null, "Deseja gerar o Extrato da Conta" + cb_contaExtrato.getSelectedItem().toString() + "?", "Atenção", JOptionPane.YES_NO_OPTION);
        Conexao.ReiniciarCon();
        if (confirma == JOptionPane.YES_OPTION) {
            Map p = new HashMap();
            JasperReport relatorio;
            JasperPrint impressao;
            JasperViewer jrviewer = null;
            try {
                if (!txt_dtInicialPrevisao.getDate().equals("")) {
                    p.put("dtInicial", txt_dtInicialPrevisao.getDate());
                    p.put("dtFinal", txt_dtFinalPrevisao.getDate());
                    p.put("idConta", getIDContaOrigem(cb_contaExtrato));
                    p.put("contaBancaria", cb_contaExtrato.getSelectedItem().toString());
                    p.put("caminhoLogo", FormatarICO.logoTipo().getImage());
                    p.put("Ordem", getOrderBy());
                    relatorio = JasperCompileManager.compileReport(new File("").getClass().getResourceAsStream("/Relatorios/Extrato_CC.jrxml"));
                    impressao = JasperFillManager.fillReport(relatorio, p, Conexao.getConnection());
                    jrviewer = new JasperViewer(impressao, false);
                    jrviewer.setVisible(true);
                    jrviewer.toFront();
                } else {
                    JOptionPane.showMessageDialog(null, "Verifique o intervalo de datas!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            this.dispose();
            jrviewer.toFront();
        }
    }//GEN-LAST:event_btn_gerarActionPerformed

