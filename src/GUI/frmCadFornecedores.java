    private void carregarBancos() {
        if (listBancos == null) {
            listBancos = new ArrayList<>();
            listBancos = DiversasD.ListBancos();
        }
        cb_banco.addItem("-");
        for (BancosBeans list : listBancos) {
            cb_banco.addItem(list);
        }
    }
    
    private void maskFormaterCPF(){
         try {
            CNPJMask = new MaskFormatter("##.###.###/####-##");
            CPFMask = new MaskFormatter("###.###.###-##");
         } catch (ParseException ex) {
            ex.printStackTrace();
        }
    }
    
    private void carregarSegmentos() {
        if (listSegmentos == null) {
            listSegmentos = new ArrayList<>();
            listSegmentos = FornecedoresD.SegmentoFornecedor();
        }
        cb_segmento.addItem("-");
        for (ListSegFornecedores list : listSegmentos) {
            cb_segmento.addItem(list);
        }
    }
   

