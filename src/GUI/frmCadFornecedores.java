    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        int editar = JOptionPane.showConfirmDialog(null, "Deseja editar este fornecedor?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (editar == JOptionPane.YES_OPTION) {
            FornB.setID(Integer.parseInt(txt_id.getText()));
            popularBeans();
            FornecedoresD.editarFornecedor(FornB);
            limparCampos();       
        }
    }//GEN-LAST:event_btn_editarActionPerformed

    private void cb_tipoPessoaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cb_tipoPessoaItemStateChanged
         if (cb_tipoPessoa.getSelectedItem().toString().equals("CPF")){
            txt_cnpj.setValue(null);
            txt_cnpj.setFormatterFactory(new DefaultFormatterFactory(CPFMask));     
         } else if(cb_tipoPessoa.getSelectedItem().toString().equals("CNPJ")){
            txt_cnpj.setValue(null);
            txt_cnpj.setFormatterFactory(new DefaultFormatterFactory(CNPJMask));
         } else if(cb_tipoPessoa.getSelectedItem().toString().equals("-")){
            txt_cnpj.setValue(null);
            txt_cnpj.setFormatterFactory(null);
        }
    }//GEN-LAST:event_cb_tipoPessoaItemStateChanged

    private void btn_pesquisarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisarCompraActionPerformed
        popularBeansPesquisa();
        FornecedoresD.buscarFornecedores(FornB, TbForn);
    }//GEN-LAST:event_btn_pesquisarCompraActionPerformed

    private void tb_fornecedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_fornecedoresMouseClicked
        preencherCampos();
        if (evt.getClickCount() == 2) {
                habilitarCampos();
                btn_editar.setEnabled(true);
        } else {
            desabilitarCampos();
            btn_Salvar.setEnabled(true);
            btn_editar.setEnabled(true);
        } 
    }//GEN-LAST:event_tb_fornecedoresMouseClicked


