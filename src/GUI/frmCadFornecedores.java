    private void popularBeans(){
        FornB.setIdSegmento(getIdSegmento());
        FornB.setNomeFantasia(txt_nomeFantasia.getText());
        FornB.setRazaoSocial(txt_razaoSocial.getText());
        FornB.setTelefone(txt_telefone.getText());
        FornB.setEmail(txt_email.getText());
        FornB.setTipoPessoa(cb_tipoPessoa.getSelectedItem().toString());
        FornB.setCnpj(txt_cnpj.getText());
        FornB.setBanco(cb_banco.getSelectedItem().toString());
        FornB.setAgencia(txt_agencia.getText());
        FornB.setConta(txt_conta.getText());
    }

    private void popularBeansPesquisa(){   
        FornB.setNomeFantasia(txt_nomeFantasia.getText());
        FornB.setRazaoSocial(txt_razaoSocial.getText());
        if (cb_tipoPessoa.getSelectedItem().equals("-")){
            FornB.setCnpj("");
        } else {
            FornB.setCnpj(txt_cnpj.getText());
        }
    }

    private int getIdSegmento(){
        if (cb_segmento.getSelectedIndex() == 0){
            return 0;
        } else {
            int index = cb_segmento.getSelectedIndex()-1;
            return listSegmentos.get(index).getId_segmento();
        }
    }

    private void preencherCampos(){
        int i = tb_fornecedores.getSelectedRow();
        txt_id.setText((TbForn.getValueAt(i, 0) == null ? "" : TbForn.getValueAt(i, 0).toString()));
        txt_nomeFantasia.setText((TbForn.getValueAt(i, 1) == null ? "" : TbForn.getValueAt(i, 1).toString()));
        txt_razaoSocial.setText((TbForn.getValueAt(i, 2) == null ? "" : TbForn.getValueAt(i, 2).toString()));
        txt_telefone.setText((TbForn.getValueAt(i, 3) == null ? "" : TbForn.getValueAt(i, 3).toString()));
        txt_email.setText((TbForn.getValueAt(i, 4) == null ? "" : TbForn.getValueAt(i, 4).toString()));
        
        cb_segmento.setSelectedIndex(setSelectedSegmento(i));
        
        cb_tipoPessoa.setSelectedItem((TbForn.getValueAt(i, 6) == null ? "" : TbForn.getValueAt(i, 6).toString()));
        txt_cnpj.setText((TbForn.getValueAt(i, 7) == null ? "" : TbForn.getValueAt(i, 7).toString()));
        cb_banco.setSelectedItem((TbForn.getValueAt(i, 8) == null ? "-" : TbForn.getValueAt(i, 8).toString()));
        txt_agencia.setText((TbForn.getValueAt(i, 9) == null ? "" : TbForn.getValueAt(i, 9).toString()));
        txt_conta.setText((TbForn.getValueAt(i, 10) == null ? "" : TbForn.getValueAt(i, 10).toString()));
    }

    private void habilitarCampos(){
            txt_nomeFantasia.setEnabled(true);
            txt_razaoSocial.setEnabled(true);
            txt_telefone.setEnabled(true);
            txt_email.setEnabled(true);
            cb_segmento.setEnabled(true);
            cb_tipoPessoa.setEnabled(true);
            txt_cnpj.setEnabled(true);
            cb_banco.setEnabled(true);
            txt_agencia.setEnabled(true);
            txt_conta.setEnabled(true);
    }

    private void desabilitarCampos(){
            txt_nomeFantasia.setEnabled(false);
            txt_razaoSocial.setEnabled(false);
            txt_telefone.setEnabled(false);
            txt_email.setEnabled(false);
            cb_segmento.setEnabled(false);
            cb_tipoPessoa.setEnabled(false);
            txt_cnpj.setEnabled(false);
            cb_banco.setEnabled(false);
            txt_agencia.setEnabled(false);
            txt_conta.setEnabled(false);
    }

    public void limparCampos(){
        txt_id.setText("");
        txt_nomeFantasia.setText("");
        txt_razaoSocial.setText("");
        txt_telefone.setText("");
        txt_email.setText("");
        cb_tipoPessoa.setSelectedItem("-");
        cb_segmento.setSelectedItem("-");
        txt_cnpj.setText("");
        cb_banco.setSelectedItem("-");
        txt_agencia.setText("");
        txt_conta.setText("");
    }

    private int setSelectedSegmento(Integer i){
        int index = 0;
        for (int j = 0; j < listSegmentos.size(); j++){
            if (listSegmentos.get(j).getId_segmento() == Integer.parseInt(TbForn.getValueAt(i,5).toString())){
                index = j+1;
                return index;
            }
        }
        return index ;
    }


